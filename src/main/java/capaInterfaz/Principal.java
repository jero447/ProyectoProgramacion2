    private void btnCargarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarEmpleadoActionPerformed
        Empleado em = new Empleado();
        try {
            em.setNombre(textNomEmpleado.getText());
            em.setApellido(textApellEmpleado.getText());
            em.setDni(Integer.parseInt(textDniEmpleado.getText()));
            em.setDireccion(textDirecEmpleado.getText());
            em.setTelefono(Long.parseLong(textTelEmpleado.getText()));
            em.setNumeroDeLegajo(Integer.parseInt(textNroLegEmpleado.getText()));
            
            java.util.Date fechaAntigua = dateFechaEmpleado.getDate();
            if (fechaAntigua != null) {
                long tiempo = fechaAntigua.getTime();
                java.sql.Date fechaIntermedia = new java.sql.Date(tiempo);
                LocalDate fechaModerna = fechaIntermedia.toLocalDate();
                em.setFechaDeIngreso(fechaModerna);
            } else {
                JOptionPane.showMessageDialog(null, "Por favor, seleccione una fecha de ingreso.");
            }
            
            ManejadorEmpleados.guardarEmpleado(em);
            modeloEmpleado.addRow(new Object[]{em.getIdEmpleado(), em.getNombre(), em.getApellido(), em.getDni(), em.getDireccion(), em.getTelefono(), em.getNumeroDeLegajo(), em.getFechaDeIngreso()});
            PanelUtilidad.limpiarCampos(jPanel14);
            PanelUtilidad.desactivarCampos(this);
            btnCargarEmpleado.setEnabled(false);
            ManejadorArchivoEmpleados.guardarEmpleadoArchivo();
            llenarComboEmpleados();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error al convertir n√∫meros: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCargarEmpleadoActionPerformed

