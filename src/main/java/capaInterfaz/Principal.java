    public void llenarTablaCompra() {
        modeloCompra.setRowCount(0);
        String nombreMaterial = "";
        Integer cantidadMat = 0;
        for (Compra compra : ManejadorCompras.getListaCompras()) {
            for (Map.Entry<Integer, Integer> entry : compra.getMaterial_Cant().entrySet()) {
                for (Materiales ma : ManejadorMateriales.getListaMateriales()) {
                    if (ma.getIdMaterial() == entry.getKey()) {
                        nombreMaterial = ma.getNombre();
                    }
                }
                cantidadMat = entry.getValue();
            }
            modeloCompra.addRow(new Object[]{
                compra.getIdCompra(),
                compra.getComprador(),
                compra.getEmpleadoInvolucrado(),
                nombreMaterial,
                cantidadMat,
                compra.getFechaDeCompra(),
                compra.getPagada(),
                compra.getSumaTotal(),});
        }
    }
    
