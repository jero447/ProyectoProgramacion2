    private void btnCargarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarCompraActionPerformed
        Compra compra = new Compra();
        Comprador comprador = new Comprador();
        Empleado empleado = new Empleado();
        Materiales material = new Materiales();
        Map<Integer, Integer> materialCantidad = new HashMap<>();
        Integer sumaTotal;
        Integer cantidad;
        String pagada = "";
        
        java.util.Date fechaAntiguaCompra = dateFechaCompra.getDate();
        if (fechaAntiguaCompra != null) {
            long tiempo = fechaAntiguaCompra.getTime();
            java.sql.Date fechaIntermediaCompra = new java.sql.Date(tiempo);
            LocalDate fechaModernaCompra = fechaIntermediaCompra.toLocalDate();
            compra.setFechaDeCompra(fechaModernaCompra);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fecha de ingreso.");
        }
        comprador = (Comprador) comboCompradores.getSelectedItem();
        empleado = (Empleado) comboEmpleados.getSelectedItem();
        material = (Materiales) comboMateriales.getSelectedItem();
        cantidad = (Integer) spinnerCant.getValue();
        sumaTotal = material.getPrecioCompra() * cantidad;
        compra.setSumaTotal(sumaTotal);
        materialCantidad.put(material.getIdMaterial(), cantidad);
        compra.setComprador(comprador);
        compra.setEmpleadoInvolucrado(empleado);
        compra.setMaterial_Cant(materialCantidad);
        ManejadorCompras.guardarCompra(compra);
        material.setStock(material.getStock() + cantidad);
        for (Materiales ma : ManejadorMateriales.getListaMateriales()) {
            if (ma.getIdMaterial() == material.getIdMaterial()) {
                ma.setStock(material.getStock());
            }
        }
        llenarTablaMateriales();
        ManejadorArchivoMateriales.guardarEmpleadoArchivo();
        comprador.setCantCompras(comprador.getCantCompras() + 1);
        for (Comprador com : ManejadorCompradores.getListaCompradores()) {
            if (com.getIdComprador() == comprador.getIdComprador()) {
                com.setCantCompras(comprador.getCantCompras());
                System.out.println(com.getCantCompras());
            }
        }
        if(radioPagadaSi.isSelected()){
            pagada = "Si";
        }else{
            pagada = "No";
        }
        compra.setPagada(pagada);
        llenarTablaComprador();
        ManejadorArchivoComprador.guardarCompradorArchivo();
        ManejadorArchivoCompras.guardarCompraArchivo();
        PanelUtilidad.desactivarCampos(jPanel7);
        modeloCompra.addRow(new Object[]{compra.getIdCompra(), compra.getComprador().getNombre() + " " + compra.getComprador().getApellido(), compra.getEmpleadoInvolucrado().getNombre() + " " + compra.getEmpleadoInvolucrado().getApellido(), material.getNombre(), cantidad, compra.getFechaDeCompra(), compra.getPagada() ,compra.getSumaTotal()});
        

    }//GEN-LAST:event_btnCargarCompraActionPerformed

